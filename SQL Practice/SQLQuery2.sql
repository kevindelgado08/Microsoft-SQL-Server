--Tipos de datos

char	--almacenan tipo de datos de ancho fijo, si sobra espacio el sistema no lo elimina, ahi queda.
varchar --almacena tipo de datos alfanumericos de ancho variable, si sobra espacio el sistema no lo elimina, ahi queda..
text	--almacena tipos de datos texto.
nchar	--almacena tipo de datos de ancho fijo, si sobra espacio el sistema lo elimina.
nvarchar	--almacena tipo de datos alfanumericos de ancho variable,  si sobra espacio el sistema lo elimina.
bit		--almacena valores de 1 y 0.
int		--almacena valores entre -2,147,483,648 y 2,147,483,647. (tipo entero)
bigint	--almacena valores entre -9,223,372,036,854,775,808 y 9,223,372,036,854,775,807.
decimal --almacena valores entre -10^38+1 y -10^38-1. (tipo decimal)
numeric --almacena valores entre -10^38+1 y -10^38-1. (aumentar de valores entreros a decimales)
money	--almacena valores entre -9,223,372,036,854,775,808 y 9,223,372,036,854,775,807. (con datos de informacion financiera)
float	--almacena valores entre -1.79E+308 to 1.79E+308.
